PORTB	EQU	06H
PORTC	EQU	07H
EEDATA	EQU	08H
EEADR	EQU	09H
PCLATH	EQU	0AH
INTCON	EQU	0BH
; BANK 1
OPTINOR	EQU	81H
TRISA	EQU	85H
TRISB	EQU	86H
TRISC	EQU	87H
EECON1	EQU	88H
EECON2	EQU	89H
ADCON1	EQU	9FH
;
IRP		EQU	7
RP1		EQU	6
RP0		EQU	5
NOT_TO	EQU	4
NOT_PD	EQU	3
ZF		EQU	2
DC		EQU	1
CF		EQU	0
;
;

W		EQU	B'0'
F		EQU	.1
DBUF1 	EQU 24H ;GPR레지스터에 저장 (중요)
DBUF2 	EQU 25H ;GPR레지스터에 저장 (중요)
DBUF3 	EQU 26H ;GPR레지스터에 저장 (중요)
COM_B	EQU 27H
COM_C	EQU	28H

;MAIN PROGRAM
ORG		0000

BSF		STATUS,RP0
MOVLW	B'00000000'
MOVWF	TRISA
MOVLW	B'00000000'
MOVWF	TRISB
MOVLW	B'00000000'
MOVWF	TRISC
MOVLW	B'00000111'
MOVWF	ADCON1

BCF		STATUS,RP0

MOVLW	0A3H
MOVWF	COM_B
;BSF		PORAT,0
CALL	RS232C
CALL	DELAY

MOVLW	.1
MOVWF	COM_B
;BSF		PORAT,0
CALL	RS232C
CALL	DELAY

;표시위치 지정
MOVLW	0A1H
MOVWF	COM_B
BSF		PORTA,0
CALL	RS232C
CALL	DELAY
MOVLW	.10
MOVWF	COM_B
BSF		PORTA,0
CALL	RS232C
CALL	DELAY
MOVLW	.0
MOVWF	COM_B
BSF		PORTA,0
CALL	RS232C
CALL	DELAY

;글쓰기 시작
MOVLW	0A2H
MOVWF	COM_B
BSF		PORTA,0
CALL	RS232C

;우리 쓰기
MOVLW	B'10110100'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY
MOVLW	B'11110001'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY
;
MOVLW	B'10101111'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY
MOVLW	B'01110111'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY
;
MOVLW	B'11010001'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY
MOVLW	B'10100001'
MOVWF	COM_B
CALL	RS232C
CALL	DELAY


BBLP
	GOTO	BBLP
	
	MOVLW	00H
	MOVWF	COM_B
	BSF		PORTA,0
	CALL	RS232C
	CALL	DELAY
	
RS232C
	MOVLW	.8
	MOVWF	COM_C
	BCF		PORTA,0
	CALL	DELAY

R_LP	
	BTFSS	COM_B,0
	GOTO	BITX
	BSF		PORTA,0
	GOTO	NEXT1
BITX
	BCF		PORTA,0
NEXT1
	CALL	DELAY
	
	RRF		COM_B,F
	DECFSZ	COM_C
	GOTO	R_LP
	
	BSF		PORTA,0
	CALL	DELAY
	RETURN	
	
;SUBROUTINE
DELAY
	MOVLW 	.1
	MOVWF	DBUF1
LP1
	MOVLW	.49
	MOVWF	DBUF2
LP2
	NOP
	DECFSZ	DBUF2,F
	GOTO	LP2
	DECFSZ	DBUF1,F
	GOTO	LP1
	RETURN
	
	END
