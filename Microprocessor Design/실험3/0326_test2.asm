; STANDARD HEADER FILE
	PROCESSOR	16F876
;
; BANK 0
INDF	EQU	00H
TMR0	EQU	01H
PCL		EQU	02H
STATUS	EQU	03H
FSR		EQU	04H
PORTA	EQU	05H
PORTB	EQU	06H
PORTC	EQU	07H
EEDATA	EQU	08H
EEADR	EQU	09H
PCLATH	EQU	0AH
INTCON	EQU	0BH
; BANK 1
OPTINOR	EQU	81H
TRISA	EQU	85H
TRISB	EQU	86H
TRISC	EQU	87H
EECON1	EQU	88H
EECON2	EQU	89H
ADCON1	EQU	9FH
;
IRP		EQU	7
RP1		EQU	6
RP0		EQU	5
NOT_TO	EQU	4
NOT_PD	EQU	3
ZF		EQU	2
DC		EQU	1
CF		EQU	0
;
;

W		EQU	B'0'
F		EQU	.1
DBUF1	EQU	24H
DBUF2	EQU	25H
DBUF3	EQU 26H
LED1	EQU	27H
LED2	EQU	28H
;
;MAIN PROGRAM
	ORG	0000
	BSF		STATUS,RP0
	MOVLW	B'00000000'
	MOVWF	TRISA
	MOVLW	B'00000000'
	MOVWF	TRISB
	MOVLW	B'00000000'
	MOVWF	TRISC
	MOVLW	B'00000111'
	MOVWF	ADCON1
	BCF		STATUS,RP0
	CLRF	PORTA
	
	MOVLW	B'00000001'
	ADDLW	00
	MOVWF	LED1
	
	MOVLW	B'00000001'
	ADDLW	00
	MOVWF	LED2
	
	MOVLW	B'00001111'
	MOVWF	PORTA
	
	MOVLW	B'11111111'
	MOVWF	PORTC
	
	

LOOP1
	MOVF	LED1,W
	;XORLW	B'11111111'
	MOVWF	PORTA
	RLF		LED1,F
	CALL	DELAY
	BTFSS	LED1,5
    GOTO	LOOP1
    GOTO	LOOP2
LOOP2
	MOVF	LED2,W
	;XORLW	B'11111111'
	MOVWF	PORTC
	RLF		LED2,F
	CALL	DELAY
	BTFSS	LED2,8
	GOTO	LOOP2
	GOTO	LOOP1

;SUBROUTINE
DELAY
	MOVLW	.255
	MOVWF	DBUF1
LP1	MOVLW	.100
	MOVWF	DBUF2
LP2	MOVLW	.10
	MOVWF	DBUF3
LP3	NOP
	DECFSZ	DBUF3,F
	GOTO	LP3
	DECFSZ	DBUF2,F
	GOTO	LP2
	DECFSZ	DBUF1,F
	GOTO	LP1
	RETURN
END